public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */
    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 13;
        int numTotal = num1 + num2;
        double numAverage =  numTotal/2.0;

        System.out.println("The average of 10 and 13 is: " + numAverage + " !");
    }
}
